package Strings;

public class SubstringsOfAString {
	 public static void main(String args[]){
	      String str="SubStringOfAString";
	      System.out.println( str.replace( 'A','a' ) );
	      System.out.println( str.replaceFirst("bS", "bs") );
	      System.out.println( str.replaceAll("ng", "NG") );
	   }

}
